  subs = []
  if project['sublinks']
    project['sublinks'].each do |link|
      subs.push("<a href=\"#{link['url']}\" target=\"_blank\">#{link['name']}</a>")
    end
  end
  projects_array.push("<span>- <a href=\"#{project['github']}\" target=\"_blank\">`#{project['name']}`</a> #{project['description']} #{subs.join(', ')}</span>")
end

